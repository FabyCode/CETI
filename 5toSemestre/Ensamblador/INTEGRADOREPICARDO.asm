	ORG 0000H
	LJMP INICIALIZACION
	ORG 0003H
	LJMP INTtres
	ORG 0013H
	LJMP INTuno
INICIALIZACION:
	MOV IE,#85H
	MOV IP,#4H
	RS EQU P3.0
	E EQU P3.1
	R8 EQU 80H
	CLR E
	CLR RS
	MOV P1, #00111000B
	LCALL RETARD
	MOV P1, #00111000B
	LCALL RETARD
	MOV P1, #00111000B
	LCALL RETARD
	MOV P1, #00001111B
	LCALL RETARD
	MOV P1, #00000001B
	LCALL RETARD
	MOV P1, #00000110B
	LCALL RETARD
	SETB RS
AQUI: 	MOV A, #00H
	MOVC A,@A+DPTR
	MOV P1,A
	INC DPTR
	LCALL RETARD1Seg
	CJNE A,#00H,AQUI
	CLR RS
	LCALL RETARD1Seg
	LJMP NAME
INTtres:
	CLR RS
	MOV P1,#01H
	LCALL RETARD
	SETB RS
	MOV DPTR, #Bote
PINdoce:
	MOV A, #00H
	MOVC A,@A+DPTR
	MOV P1,A
	INC DPTR
	LCALL RETARD
	CJNE A,#00H, PINdoce
	CLR RS
	LCALL RETARD1Seg
	RETI
INTuno:	CLR RS
	MOV P1,#01H
	LCALL RETARD
	SETB RS
	MOV DPTR, #Caja
PINtrece:
	MOV A, #00H
	MOVC A,@A+DPTR
	MOV P1,A
	INC DPTR
	LCALL RETARD
	CJNE A,#00H, PINtrece
	CLR RS
	LCALL RETARD1Seg
	RETI
Bote:	DB 'Botellas: '
Caja:	DB 'Cajas: '
RETARD:
	SETB E
	MOV R2, #0FAH
BUCLE:	MOV R3, #0FAH
BUCLE2:	DJNZ R3,BUCLE2
	DJNZ R2, BUCLE
	CLR E
	MOV R2,#0FAH
	MOV R3,#0FAH
	RET
RETARD1Seg:
	SETB E
	MOV R1,#08H
PACA:	MOV R2, #0FAH
ACA:	MOV R3, #0FAH
ACATLAN:
	DJNZ R3,ACATLAN
	DJNZ R2, ACA
	DJNZ R1,PACA
	MOV R1,#08H
	MOV R2,#0FAH
	MOV R3,#0FAH
	CLR E
	RET
	END